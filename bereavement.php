						<h2>The grieving cycle<b class="pull-right">(2)</b></h2>
						<p>​Soon though, this numbness can disappear and may be replaced by a dreadful sense of agitation, of pining or yearning for the dead person. There is a feeling of wanting somehow to find them, even though this is clearly impossible. 
						This makes it difficult to relax or concentrate and it may be difficult to sleep properly. Dreams may be extremely disturbing. Some people feel that they ‘see’ their loved one everywhere they go, in the street, the park, around the house, anywhere they had spent time together. 
						People often feel very angry at this time – towards doctors and nurses who did not prevent the death, towards friends and relatives who did not do enough, or even towards the person who has left them.</p>
						<p>Another common feeling is guilt. People find themselves going over in their minds all the things they would have liked to have said or done. 
						They may even consider what they could have done differently that might have prevented the death. Of course, death is usually beyond anyone’s control and a bereaved person may need to be reminded of this. 
						Guilt may also arise if a sense of relief is felt when someone has died after a particularly painful or distressing illness. This feeling of relief is natural, extremely understandable and very common. </p>
						<h3>Bereavement counselling can help you work through these complex feelings.</h3>
