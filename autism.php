						It is common for autistic people to have extremely high skills in certain areas and have a lot of trouble with other areas.</p>
						<p> People with Autism can have trouble with executive function – the set of abilities that enable people to work towards goals.
						These include time management, initiating appropriate actions, inhibiting inappropriate actions, flexibility and selecting relevant sensory information. This can make it difficult to organise an everyday task.</p>
						<p> Instead of visualising a list of tasks, a person with Autism or Asperger’s might remember everything they need to do at random and at different times, which can feel chaotic.
						Everyday tasks such as this can feel very tiring, which might mean that autistic people struggle with persevering at such tasks.</p>
