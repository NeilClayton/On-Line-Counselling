						<h3>Who does it affect?</h3>
						<p>The disorder can affect all age groups, though it typically starts during puberty when people are most sensitive about the way they look. It is thought that body dysmorphic disorder affects more women than men, but with more and more men speaking out about mental illness, these numbers may change.</p>
						<h3>What causes body dysmorphic disorder?</h3>
						<p>Like many anxiety disorders, BDD has no clear-cut cause. Genetics may play a part or there could be a chemical imbalance in the brain. Those with a tendency to anxiety and obsessive compulsive disorder (OCD) may also be more likely to develop body dysmorphic disorder. </p>
						<p>In some cases those with an eating disorder can develop body dysmorphic disorder and vice-versa, however the two are not the same condition. Past experience could also contribute to the condition, especially if the person was bullied or teased about an aspect of their appearance when they were younger.</p>
