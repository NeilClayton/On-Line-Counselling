					<section class="col-sm-10 col-sm-offset-1 col-md-8 col-md-offset-2">
						<h2>What are unhelpful thinking patterns?<b class="pull-right">(3)</b></h2>
						<h3>Shoulds or Musts</h3>
						<p>Shoulding and musting is where you focus on how you perceive things ‘should’ or ‘must’ be, rather than how it is. Shoulding and musting can pressure you to do things one particular way or the ‘right way’. 
						These might be pressures regarding yourself, or other people in your life.</p>
						<h3>Totalising or Labelling</h3>
						<p>Totalising thinking takes a single mistake, problem or shortcoming, and gets you to see yourself – your identity — entirely through that lens. (e.g. ‘I spilled my drink, I’m such a loser’). Common labels include ‘loser’, ‘idiot’, etc. 
						Sometimes this pattern of thinking has you labelling others.</p>
						<h3>Mind Reading</h3>
						<p>This is when you ‘know’ what someone else is thinking, even though you have no idea what they are thinking. It often takes the form of an assumption that another person is making a negative judgement about you.</p>
						<h3>Discounting the Positive</h3>
						<p>You reject positive experiences by insisting that they “don’t count”. For example, if you have a positive interaction with someone, you write it off as a one-off, or attribute it solely to the other person’s actions and not seeing your own part. Discounting the positives takes the joy out of life and makes you feel inadequate and unrewarded.</p>
					</section>
