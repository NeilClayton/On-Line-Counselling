						<h2>What are unhelpful thinking patterns?<b class="pull-right">(2)</b></h2>
						<h3>Stewing or Contemplating</h3>
						<p>Stewing or contemplating is where you find yourself running things repetitively over and over in your mind, like a tape loop, without any fresh input or action being taken. 
						Typically, stewing or contemplating leads to problems growing in size and appearing even more difficult to deal with.</p>
						<h3>Catastrophising and over Generalising</h3>
						<p>Catastrophising and over-generalising is where you take a single event or limited piece of information, and see it as a global pattern (usually a negative one). 
						If you hear yourself using words like ‘always’ or ‘never’, these are hints that you might be catastrophising or over generalising (e.g. ‘I’m always stuffing things up’, ‘I never get a fair go’).</p>
						<h3>All or Nothing Thinking</h3>
						<p>All or nothing thinking, or black or white thinking, is where things are either all good OR all bad. It’s either one extreme or the other; there are no grey areas.</p>
					</section>
