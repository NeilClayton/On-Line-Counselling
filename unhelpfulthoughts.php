						<h2>What are unhelpful thinking patterns?<b class="pull-right">(4)</b></h2>
						<h3>Forecasting</h3>
						<p>When you predict that something will turn out badly, or you will stuff things up, without there being any evidence. Forecasting can get in the way of taking action to make things better.</p>
						<h3>Funnelling</h3>
						<p>Funnelling is when you interpret every difficulty as a result of the abuse you experienced. For example, if you feel stressed about something at work, funnelling puts this down to some personal failure resulting from the abuse, rather than identifying that there might actually be things that would cause most people to feel stressed.</p>
						<h3>Emotional Reasoning</h3>
						<p>You assume that your negative emotions necessarily reflect the way things really are. “I feel guilty. I must be a rotten person.” Or, “I feel angry. This proves that I’m being treated unfairly.” Or, “I feel so inferior. This means I’m a second rate person.” Or, “I feel hopeless. Things must really be hopeless.”</p>
						<h3>If you recognise that any of the above thought patterns are getting in the way of you living peacefully then come and talk to me.</h3>
